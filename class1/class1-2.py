# 輸入半徑並計算圓面積
# Input the radius and calculate the area of a circle

r = float(input("請輸入圓的半徑: "))  # 提示使用者輸入半徑，並轉為浮點數
# Prompt the user to enter the radius and convert it to float

area = 3.14 * r**2  # 計算圓面積
# Calculate the area of the circle

print(f"圓面積為: {area}")  # 印出圓面積
